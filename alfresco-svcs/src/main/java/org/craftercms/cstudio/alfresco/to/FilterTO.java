package org.craftercms.cstudio.alfresco.to;

/**
 * search filter 
 * 
 * @author hyanghee
 *
 */
public class FilterTO {

	/** the filed name key to apply this filter on **/
	protected String key;
	/** the filter value **/
	protected String value;
	/** the start date if the filter is on date **/
	protected String startDate;
	/** the end date if the filter is on date **/
	protected String endDate;
	/** use wildcard text search? **/
	protected boolean useWildcard;

	/**
	 * @return the key
	 */
	public String getKey() {
		return key;
	}

	/**
	 * @param key
	 *            the key to set
	 */
	public void setKey(String key) {
		this.key = key;
	}

	/**
	 * @return the value
	 */
	public String getValue() {
		return value;
	}

	/**
	 * @param value
	 *            the value to set
	 */
	public void setValue(String value) {
		this.value = value;
	}

	/**
	 * @return the startDate
	 */
	public String getStartDate() {
		return startDate;
	}

	/**
	 * @param startDate
	 *            the startDate to set
	 */
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	/**
	 * @return the endDate
	 */
	public String getEndDate() {
		return endDate;
	}

	/**
	 * @param endDate
	 *            the endDate to set
	 */
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	/**
	 * @return the useWildcard
	 */
	public boolean isUseWildcard() {
		return useWildcard;
	}

	/**
	 * @param useWildcard
	 *            the useWildcard to set
	 */
	public void setUseWildcard(boolean useWildcard) {
		this.useWildcard = useWildcard;
	}

}
