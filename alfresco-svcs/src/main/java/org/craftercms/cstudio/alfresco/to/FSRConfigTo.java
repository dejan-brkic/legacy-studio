package org.craftercms.cstudio.alfresco.to;

/**
 * Represents the fsr configuration maintained at environment-config.xml. This configuration is currently used to identify all the configured FSR
 * in alfresco. Only successfull deployment at all these FSRs will be termed as successfull deployment
 */
public class FSRConfigTo {

    protected String name;
    protected String host;
    protected String type;

    public FSRConfigTo(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
